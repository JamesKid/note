#Linux初级讲师要求：
	1、 有2年以上大型网站系统运维相关工作经验；
	2、 具有良好的团队协作能力，心态积极，责任心；
	3、 熟悉Linux系统管理用户管理、磁盘管理、文件权限、软件包管理等；
	4、 熟悉Linux基础应用DNS，Mail，Iptables，Apache，Tomcat，Nginx等的安装、配置、优化；
	5、 熟悉开源的监控系统Zabbix，Nagios，Cacti等软件的配置和管理；
	6、 熟悉MySQL数据库的规划、部署、优化；
	7、 精通Python、Shell、Perl等常见脚本语言开发；
	8、 熟悉Chef、Puppet其中一种集中配置管理工具，具有自动化运维的思想，并有实施自动化运维工具的经验；
 
 #Linux高级讲师要求：
	 9、 必须达到Linux初级讲师技术要求；
	 10、熟悉KVM，Vmware vSphere等相关虚拟化技术；
	 11、熟悉常见的集群技术LVS，HaProxy，Nginx，Keepalived，RHCS的架构及部署；
	 12、熟悉Linux系统及相关应用的调优；
	 13、熟悉云计算相关技术Openstack、Hadoop等的部署和管理；
	 14、有大型网站运维经验，具有高负载高并发量网站服务器维护经验者优先；
	 15、有培训机构讲师工作经验或企业内训经验者优先
